// script prints true if both parameters are greater than or equal to 5

let num1 = 5;
let num2 = 6;

// if both num1 and num2 are greater than
// or equal to 5 then the result is true
if (num1 >= 5 && num2 >= 5) {
	console.log(true);
} else {
	// if the conditions are not true
	// then it is false
	console.log(false);
}
